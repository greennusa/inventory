    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {   
        
        $dr = DetailReturBarangCamp::findOrFail($id);
        $c = Camp::findOrFail($dr->camp_id);
        $c->where('barang_id',$dr->barang_id)->update([
            'status'=>0,
            'stok_retur'=>0
        ]);
        
        $dr->update(['status'=>1]);
        return back();
    }
