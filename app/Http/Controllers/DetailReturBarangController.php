    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $dr = DetailReturBarang::findOrFail($id);
        DetailPemakaianBarang::where('pemakaian_barang_id',$dr->pemakaian_barang_id)->where('barang_id',$dr->barang_id)->update([
            'status'=>0,
            'jumlah_retur'=>0
        ]);
        
        $dr->update(['status'=>1]);
        return back();
    }
